public class Operaciones {

	
	/**
	 * @param n1 Primer numero
	 * @param n2 Segundo numero
	 * @return op Es el resulktado de la multiplicacion de n1 y n2
	 * @since 1.1
	 */
	public int multiplicacion(int n1, int n2){
		int op;
		op = n1 * n2;
		
		return op;
	}
	
	
	
}
